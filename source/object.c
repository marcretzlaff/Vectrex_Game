#include "object.h"

// ---------------------------------------------------------------------------

void init_object(struct object_t* p)
{
	p->status = ACTIVE;

	//random new hole to fly through
	p->y = (int) (Random() & 0b01111111);
	
	
	p->x = 120; //appears from right side of screen
}
// ---------------------------------------------------------------------------

void move_object(struct object_t* p)
{
	if (current_level.frame != 0)
	{
		(p->x)--;
	}
}

